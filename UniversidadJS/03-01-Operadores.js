// Operadores aritméticos
let a = 3,
  b = 2;
// Suma
let z = a + b;
console.log('resultado de la suma: ' + z);
// Resta
z = a - b;
console.log('resultado de la resta: ' + z);
// Multiplicación
z = a * b;
console.log('resultado de la multiplicación: ' + z);
// División
z = a / b;
console.log('resultado de la división: ' + z);
// Módulo
z = a % b;
console.log('resultado del módulo: ' + z);
// Potencia
z = b ** b;
console.log('resultado de la potencia: ' + z);

// Incremento
a++;
console.log('resultado del incremento: ' + a);
// Pre-incremento
z = ++a;
console.log('resultado del pre-incremento: ' + a);
console.log('resultado del pre-incremento: ' + z);
//Post-incremento
z = b++;
console.log('resultado del post-incremento: ' + b);
console.log('resultado del post-incremento: ' + z);
// Decremento
a--;
console.log('resultado del decremento: ' + a);
// Pre-decremento
z = --a;
console.log('resultado del pre-decremento: ' + a);
console.log('resultado del pre-decremento: ' + z);
//Post-decremento
z = b--;
console.log('resultado del post-decremento: ' + b);
console.log('resultado del post-decremento: ' + z);
